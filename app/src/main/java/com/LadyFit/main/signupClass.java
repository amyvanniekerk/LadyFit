package com.LadyFit.main;

public class signupClass {

    String name;
    String surname;
    String email;
    String username;
    String createPass;
    String verifyPass;

    public signupClass(String name, String surname, String email, String username, String createPass, String verifyPass) {
        this.name = name;
        this.surname = surname;
        this.email = email;
        this.username = username;
        this.createPass = createPass;
        this.verifyPass = verifyPass;
    }
}
